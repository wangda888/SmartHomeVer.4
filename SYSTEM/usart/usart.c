u16 USART_RX_STA=0;       //接收状态标记	  

//初始化IO 串口1 
//bound:波特率
void uart_init(u32 bound){
    //GPIO端口设置
	GPIO_InitPara GPIO_InitStructure;
	USART_InitPara USART_InitStructure;
	NVIC_InitPara NVIC_InitStructure;
	 
	RCC_AHBPeriphClock_Enable(RCC_AHBPERIPH_GPIOB, ENABLE);	//使能GPIOA时钟
	RCC_APB2PeriphClock_Enable(RCC_APB2PERIPH_USART1,ENABLE);//使能USART1时钟

	GPIO_PinAFConfig(GPIOB,GPIO_PINSOURCE6,GPIO_AF_0);
	GPIO_PinAFConfig(GPIOB,GPIO_PINSOURCE7,GPIO_AF_0);
 //USART1_TX   PA.9 USART1_RX	  PA.10
	GPIO_InitStructure.GPIO_Pin = GPIO_PIN_6|GPIO_PIN_7; //PA.9 PA.10
	GPIO_InitStructure.GPIO_Speed = GPIO_SPEED_50MHZ;
	GPIO_InitStructure.GPIO_Mode = GPIO_MODE_AF;	//复用推挽输出
	GPIO_InitStructure.GPIO_OType = GPIO_OTYPE_PP;
	GPIO_InitStructure.GPIO_PuPd = GPIO_PUPD_PULLUP;
	GPIO_Init(GPIOB, &GPIO_InitStructure); //初始化PA9
  
   //USART 初始化设置

	USART_InitStructure.USART_BRR = bound;//一般设置为9600;
	USART_InitStructure.USART_WL = USART_WL_8B;//字长为8位数据格式
	USART_InitStructure.USART_STBits = USART_STBITS_1;//一个停止位
	USART_InitStructure.USART_Parity = USART_PARITY_RESET;//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HARDWAREFLOWCONTROL_NONE;//无硬件数据流控制
	USART_InitStructure.USART_RxorTx = USART_RXORTX_RX | USART_RXORTX_TX;	//收发模式

  USART_Init(USART1, &USART_InitStructure); //初始化串口
	
	USART_Enable(USART1, ENABLE);  
	USART_INT_Set(USART1, USART_INT_RBNE, ENABLE);//开启中断
//Usart1 NVIC 配置

	NVIC_InitStructure.NVIC_IRQ = USART1_IRQn;
	NVIC_InitStructure.NVIC_IRQPreemptPriority=3 ;//抢占优先级3
	NVIC_InitStructure.NVIC_IRQSubPriority = 3;		//子优先级3
	NVIC_InitStructure.NVIC_IRQEnable = ENABLE;			//IRQ通道使能
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器
  
	                  //使能串口 
}

void USART1_IRQHandler(void)                	//串口1中断服务程序
	{
	u8 Res;
#ifdef OS_TICKS_PER_SEC	 	//如果时钟节拍数定义了,说明要使用ucosII了.
	OSIntEnter();    
#endif
	if(USART_GetIntBitState(USART1, USART_INT_RBNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
		{
		Res = USART_DataReceive(USART1);//(USART1->DR);	//读取接收到的数据
		
		if((USART_RX_STA&0x8000)==0)//接收未完成
			{
			if(USART_RX_STA&0x4000)//接收到了0x0d
				{
				if(Res!=0x0a)USART_RX_STA=0;//接收错误,重新开始
				else USART_RX_STA|=0x8000;	//接收完成了 
				}
			else //还没收到0X0D
				{	
				if(Res==0x0d)USART_RX_STA|=0x4000;
				else
					{
					USART_RX_BUF[USART_RX_STA&0X3FFF]=Res ;
					USART_RX_STA++;
					if(USART_RX_STA>(USART_REC_LEN-1))USART_RX_STA=0;//接收数据错误,重新开始接收	  
					}		 
				}
			}   		 
     } 
#ifdef OS_TICKS_PER_SEC	 	//如果时钟节拍数定义了,说明要使用ucosII了.
	OSIntExit();  											 
#endif
} 
